{
	ListNode_t node;
	Id_t id = ID_INVALID;
	Id_t exp_id = 2;
	
	memset(&node, 1, sizeof(node));
	
	node.id = exp_id;
	
	id  = ListNodeIdGet(&node);
	TEST_ASSERT_EQUAL(id, exp_id);
}