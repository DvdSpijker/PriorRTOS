void test_IdListInit_valid(void)
{
	IdList_t list;
	IdListInit(&list);
	TEST_ASSERT_EQUAL(list.n, ID_LIST_EMPTY);
	for(uint8_t i = 0; i < ID_LIST_SIZE_MAX; i++) {
		TEST_ASSERT_EQUAL(list.ids[i], ID_INVALID);
	}
}

